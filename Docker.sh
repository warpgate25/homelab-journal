Сегодня начал собирать домашнюю лабораторию, цель:
Подготовить Ubuntu Server, установить Docker, разобраться с принципом работы контейнеров и развернуть первый сервис.

Установка Docker

Для установки использовал официальный установочный скрипт Docker.

Скачал скрипт:

curl -fsSL https://get.docker.com -o get-docker.sh

После этого выполнил установку:

sudo sh get-docker.sh
Первая ошибка

При первом запуске допустил ошибку — случайно объединил две команды в одну строку.

Вместо последовательного выполнения:

curl -fsSL https://get.docker.com -o get-docker.sh
sudo sh get-docker.sh

команды были введены вместе, из-за чего curl попытался интерпретировать слова sudo и sh как адреса серверов.

Получил ошибки вида:

Could not resolve host: sudo
Could not resolve host: sh

Исправление оказалось простым — выполнять команды по отдельности.

После повторного запуска установка прошла успешно.

Проверка установки

После завершения установки проверил установленную версию Docker:

docker version

Получил информацию о Docker Engine, Client и Server, что подтвердило успешную установку.

Настройка доступа пользователя

Следующим шагом попытался выполнить:

docker ps

Получил ошибку:

permission denied while trying to connect to the Docker daemon socket

Это означало, что Docker установлен, но текущий пользователь не имеет доступа к сокету Docker.

Поиск причины

Начал диагностику.

Проверил текущие группы пользователя:

groups

Проверил информацию о пользователе:

id

Проверил права на сокет Docker:

ls -l /var/run/docker.sock

Убедился, что сокет принадлежит группе docker.

Проверил существование группы:

getent group docker

Вывод показал:

docker:x:988:kali

Это означало, что пользователь уже добавлен в группу Docker.

Вторая ошибка

Во время настройки допустил ещё одну ошибку.

Вместо команды:

sudo usermod -aG docker kali

случайно написал:

sudo usermode -aG docker kali

Получил сообщение:

command not found

После проверки командой:

which usermod

стало понятно, что проблема была обычной опечаткой.

Исправил команду и повторил настройку.

Проверка служб Docker

Проверил состояние службы:

sudo systemctl status docker

Убедился, что служба находится в состоянии:

active (running)

Также убедился, что Docker запускается автоматически после загрузки системы.

Проверка групп пользователя

После повторного входа в систему выполнил:

id

Убедился, что пользователь состоит в группе:

docker

После этого команды Docker начали работать без использования sudo.

Первый контейнер

Для проверки работы Docker выполнил:

docker run hello-world

Docker автоматически:

подключился к Docker Hub;
скачал образ;
создал контейнер;
выполнил программу внутри контейнера;
вывел приветственное сообщение.

Это подтвердило корректную работу Docker Engine.

Изучение образов и контейнеров

Посмотрел скачанные образы:

docker images

Увидел локальный образ:

hello-world

После этого посмотрел список контейнеров:

docker ps -a

Увидел созданный контейнер hello-world, который завершил работу после выполнения программы.

Разобрался, что:

Image — это шаблон приложения;
Container — запущенный экземпляр этого шаблона.
Развёртывание первого веб-сервера

Следующим этапом развернул Nginx.

Выполнил:

docker run -d --name nginx-server -p 80:80 nginx

Docker автоматически скачал официальный образ Nginx с Docker Hub.

После завершения загрузки проверил:

docker ps

Контейнер успешно работал в состоянии:

Up
Проверка работы веб-сервера

Открыл браузер на основном компьютере и перешёл по адресу виртуальной машины:

http://192.168.1.181

Получил стандартную страницу:

Welcome to nginx!

Это подтвердило, что:

контейнер работает;
проброс портов выполнен правильно;
сеть VirtualBox настроена корректно;
веб-сервер доступен с другого устройства.
